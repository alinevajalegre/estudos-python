print('É ASCII? -->', variable.isascii())
print('Method returns True if all characters in the string are alphanumeric ---> ', variable.isalnum())
print('Está toda em caracteres minusculos -->', variable.islower())
print('Está toda em caracteres maiusculos -->', variable.isupper())
print('É espaço -->', variable.isspace())

